-- Creación de tablas --
drop table if exists `Clientes`;
create table `Clientes` (
`idCliente` int not null auto_increment,
`NombreCliente` varchar(45) not null,
`DireccionCliente` varchar(45) not null,
`CIF` varchar(10) not null unique,
`Telefono` varchar(20) not null,
`Email` varchar(45) not null,
`Observaciones` text,
`Date_created` datetime default now() not null,
`Date_modified` datetime default now() not null,
primary key (`idCliente`)
);

drop table if exists `DireccionesEntrega`;
create table `DireccionesEntrega` (
`idDireccionEntrega` int not null auto_increment,
`LineaDireccion1` varchar(45) not null,
`LineaDireccion2` varchar(45),
`CodigoPostal` varchar(45) not null,
`Ciudad` varchar(45) not null,
`Provincia` varchar(45) not null,
`Pais` varchar(45) not null,
`Clientes_idCliente` int not null ,
primary key (`idDireccionEntrega`) ,
foreign key (`Clientes_idCliente`) references `Clientes`(`idCliente`) on delete cascade
);

drop table if exists `Envios`;
create table `Envios` (
`idEnvio` int not null auto_increment,
`Destinatario` varchar(45) not null,
`InstruccionesEntrega` text,
`Medidas` varchar(45) not null,
`Peso` int not null,
`Clientes_idCliente` int not null,
`DireccionesEntrega_idDireccionEntrega` int not null,
primary key (`idEnvio`),
foreign key (`Clientes_idCliente`) references `Clientes`(`idCliente`) on delete cascade,
foreign key (`DireccionesEntrega_idDireccionEntrega`) references `DireccionesEntrega`(`idDireccionEntrega`) on delete cascade
);

drop table if exists `Estados`;
create table `Estados` (
`idEstadosEnvio` char(3) not null ,
`Estado` varchar(45) not null, 
primary key (`idEstadosEnvio`)
);

drop table if exists `EstadosEnvio`;
create table `EstadosEnvio` (
`EstadosEnvio_idEstadosEnvio` char(3) not null,
`Envios_idEnvio` int not null,
`Fecha` datetime not null,
foreign key (`EstadosEnvio_idEstadosEnvio`) references `Estados`(`idEstadosEnvio`) on delete cascade, 
foreign key (`Envios_idEnvio`) references `Envios`(`idEnvio`) on delete cascade
);

